def get_xgb_importance_std(model: "xgb.XGBRegressor", feature_names: List[str]) -> np.ndarray:
    """Compute per-feature importance std across individual XGBoost trees."""
    df_trees = model.get_booster().trees_to_dataframe()
    splits = df_trees[df_trees["Feature"] != "Leaf"].copy()
    per_tree = (
        splits.groupby(["Tree", "Feature"])["Gain"]
        .sum()
        .unstack(fill_value=0.0)
        .reindex(columns=feature_names, fill_value=0.0)
    )
    return per_tree.std(axis=0).values


def get_lgb_importance_std(model: "lgb.LGBMRegressor", feature_names: List[str]) -> np.ndarray:
    """Compute per-feature importance std across individual LightGBM trees."""
    model_json = model.booster_.dump_model(num_iteration=model.booster_.num_trees())
    trees = model_json.get("tree_info", [])

    feat_index = {f: i for i, f in enumerate(feature_names)}
    n_trees = len(trees)
    per_tree = np.zeros((n_trees, len(feature_names)))

    def walk(node: dict, tree_idx: int) -> None:
        if "split_feature" in node:
            feat_name = feature_names[node["split_feature"]]
            col = feat_index.get(feat_name)
            if col is not None:
                per_tree[tree_idx, col] += node.get("split_gain", 0.0)
            walk(node["left_child"], tree_idx)
            walk(node["right_child"], tree_idx)

    for tree_idx, tree in enumerate(trees):
        walk(tree["tree_structure"], tree_idx)

    return per_tree.std(axis=0)


def get_catboost_importance_std(
    model_params: dict,
    X_train: pd.DataFrame,
    y_train: pd.Series,
    cat_features: List[str],
    n_bootstrap: int = 20,
    random_state: int = 42,
) -> np.ndarray:
    """Compute per-feature importance std for CatBoost via bootstrap resampling."""
    logger.info("Computing CatBoost error bars via %d bootstrap resamples...", n_bootstrap)
    rng = np.random.default_rng(random_state)
    importances = []

    for _ in range(n_bootstrap):
        idx = rng.integers(0, len(X_train), size=len(X_train))
        X_b = X_train.iloc[idx].copy()
        y_b = y_train.iloc[idx]
        X_b = prepare_categorical_as_string(X_b, cat_features)

        params = dict(model_params)
        model = CatBoostRegressor(**params)
        try:
            model.fit(X_b, y_b)
        except Exception as exc:
            if params.get("task_type") == "GPU":
                logger.warning("CatBoost bootstrap GPU fit failed (%s). Falling back to CPU for this sample.", exc)
                params.pop("task_type", None)
                model = CatBoostRegressor(**params)
                model.fit(X_b, y_b)
            else:
                raise

        importances.append(model.get_feature_importance())

    return np.std(importances, axis=0)


def get_tabpfn_quantile_columns(q_preds: Any, quantiles: List[float]) -> List[tuple[float, np.ndarray]]:
    """Return (quantile, predictions_vector) pairs independent of output orientation."""
    values = np.asarray(q_preds)

    if values.ndim == 1:
        if len(quantiles) != 1:
            raise ValueError("TabPFN returned 1D quantile predictions for multiple quantiles.")
        return [(quantiles[0], values)]

    if values.ndim != 2:
        raise ValueError(f"Unexpected TabPFN quantile output shape: {values.shape}")

    if values.shape[1] == len(quantiles):
        return [(q, values[:, i]) for i, q in enumerate(quantiles)]

    if values.shape[0] == len(quantiles):
        return [(q, values[i, :]) for i, q in enumerate(quantiles)]

    raise ValueError(
        f"Cannot align TabPFN quantiles. output_shape={values.shape}, requested_quantiles={len(quantiles)}"
    )


